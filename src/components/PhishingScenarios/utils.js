export const getDefaultEmailTemplatePayload = (categoryResourceId = '') => {
  // Double Barrel (methodTypeId '6') maps to the Barrel category; others use the index-based lookup.
  const categoryResourceIdValue =
    String(categoryResourceId) === SCENARIO_METHOD_TYPE.DOUBLE_BARREL
      ? BARREL_EMAIL_TEMPLATE_CATEGORY_RESOURCE_ID
      : SCENARIO_METHODS[Number(categoryResourceId) - 1]?.value
  return {
    pageNumber: 1,
    pageSize: 10,
    orderBy: 'createTime',
    ascending: false,
    filter: {
      Condition: 'AND',
      FilterGroups: [
        {
          Condition: 'AND',
          FilterItems: [
            {
              value:
                categoryResourceIdValue === '4'
                  ? 'WNZt0sCVCWB3,DYC0gugxJMjT'
                  : categoryResourceIdValue,
              FieldName: 'CategoryResourceId',
              Operator: 'Include'
            },
            {
              value: '',
              FieldName: 'DifficultyResourceId',
              Operator: 'Include'
            },
            {
              value: '',
              FieldName: 'LanguageTypeResourceId',
              Operator: 'Include'
            }
          ],
          FilterGroups: []
        },
        {
          Condition: 'OR',
          FilterItems: [
            { FieldName: 'Name', Operator: 'Contains', value: '' },
            { FieldName: 'CategoryName', Operator: 'Contains', value: '' },
            { FieldName: 'DifficultyName', Operator: 'Contains', value: '' },
            { FieldName: 'CreatedBy', Operator: 'Contains', value: '' },
            { FieldName: 'Tags', Operator: 'Contains', value: '' },
            { FieldName: 'CreateTime', Operator: 'Contains', value: '' }
          ],
          FilterGroups: []
        }
      ]
    }
  }
}
